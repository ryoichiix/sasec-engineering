// Shared: batch-fetch supervisor names and attach
async function enrichWithSupervisorNames(data) {
  const ids = [...new Set((data || []).map((r) => r.supervisor_id))]
  let nameById = {}
  if (ids.length > 0) {
    const { data: profs } = await supabase
      .from('profiles')
      .select('id, full_name')
      .in('id', ids)
    for (const p of profs || []) nameById[p.id] = p.full_name
  }
  return {
    error: null,
    data: (data || []).map((r) => ({
      ...r,
      supervisor_account_name: nameById[r.supervisor_id] || r.supervisor_name || 'Supervisor',
    })),
  }
}

// ── Scan upload ───────────────────────────────────────────────

export async function uploadScan(supervisorId, formType, formId, file) {
  const ext  = file.name.split('.').pop().toLowerCase() || 'jpg'
  const path = `${supervisorId}/${formType}/${formId}.${ext}`
  const { error } = await supabase.storage
    .from(SCAN_BUCKET)
    .upload(path, file, { contentType: file.type, upsert: true })
  if (error) return { error, path: null }
  return { error: null, path }
}

export async function getScanUrl(path) {
  if (!path) return null
  const { data } = await supabase.storage
    .from(SCAN_BUCKET)
    .createSignedUrl(path, 60 * 60)
  return data?.signedUrl ?? null
}
